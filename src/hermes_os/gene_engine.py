"""GeneEngine — Oct-OS inspired evolution engine for Hermes OS.

Implements three evolutionary patterns from Oct-OS:
1. Antibody Genes — immune memory for failure patterns
2. Gene Pool — tracking successful prompt strategies
3. Differentiation — auto-spawning specialized agents for recurring failures

Usage:
    engine = GeneEngine()
    await engine.record_failure(error_pattern, fix_strategy)
    await engine.get_antibody(error_pattern)  # Returns fix if exists
    await engine.record_success(prompt, context, outcome)
    specialized = await engine.check_differentiation(failure_count)
"""

from __future__ import annotations

import json
import logging
import re
from dataclasses import dataclass, field
from datetime import UTC, datetime
from pathlib import Path
from typing import Any

logger = logging.getLogger("hermes_os.gene_engine")


# ---------------------------------------------------------------------------
# Gene Data Structures
# ---------------------------------------------------------------------------


@dataclass
class AntibodyGene:
    """An antibody gene — a fix strategy for a known failure pattern."""

    pattern: str  # Regex or key pattern that identifies this failure
    fix_strategy: str  # Prompt or approach that fixes this
    failure_count: int = 0  # How many times this pattern was encountered
    success_count: int = 0  # How many times the fix worked
    last_seen: str = field(default_factory=lambda: datetime.now(UTC).isoformat())
    created_at: str = field(default_factory=lambda: datetime.now(UTC).isoformat())

    @property
    def effectiveness(self) -> float:
        """Effectiveness score = success / failure ratio."""
        return self.success_count / max(1, self.failure_count)

    def to_dict(self) -> dict[str, Any]:
        return {
            "pattern": self.pattern,
            "fix_strategy": self.fix_strategy,
            "failure_count": self.failure_count,
            "success_count": self.success_count,
            "last_seen": self.last_seen,
            "created_at": self.created_at,
            "effectiveness": self.effectiveness,
        }

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> AntibodyGene:
        return cls(
            pattern=data["pattern"],
            fix_strategy=data["fix_strategy"],
            failure_count=data.get("failure_count", 0),
            success_count=data.get("success_count", 0),
            last_seen=data.get("last_seen", datetime.now(UTC).isoformat()),
            created_at=data.get("created_at", datetime.now(UTC).isoformat()),
        )


@dataclass
class SuccessfulGene:
    """A successful prompt strategy that can be reused."""

    prompt_template: str
    context_type: str  # e.g., "code_generation", "research", "review"
    outcome: str  # "success" | "partial" | "failed"
    use_count: int = 0
    success_rate: float = 0.0
    last_used: str = field(default_factory=lambda: datetime.now(UTC).isoformat())
    created_at: str = field(default_factory=lambda: datetime.now(UTC).isoformat())

    def to_dict(self) -> dict[str, Any]:
        return {
            "prompt_template": self.prompt_template,
            "context_type": self.context_type,
            "outcome": self.outcome,
            "use_count": self.use_count,
            "success_rate": self.success_rate,
            "last_used": self.last_used,
            "created_at": self.created_at,
        }

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> SuccessfulGene:
        return cls(
            prompt_template=data["prompt_template"],
            context_type=data.get("context_type", ""),
            outcome=data.get("outcome", "success"),
            use_count=data.get("use_count", 0),
            success_rate=data.get("success_rate", 0.0),
            last_used=data.get("last_used", datetime.now(UTC).isoformat()),
            created_at=data.get("created_at", datetime.now(UTC).isoformat()),
        )


